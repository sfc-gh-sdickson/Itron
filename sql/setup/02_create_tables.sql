USE DATABASE ITRON_DB;
USE WAREHOUSE ITRON_WH;

-- ============================================================================
-- RAW SCHEMA - Operational Data
-- ============================================================================
USE SCHEMA ITRON_DB.RAW;

-- Meter Asset Registry
CREATE OR REPLACE TABLE METERS (
    METER_ID VARCHAR(50) PRIMARY KEY,
    METER_TYPE VARCHAR(20) NOT NULL COMMENT 'ELECTRIC, GAS, or WATER',
    MANUFACTURER VARCHAR(100),
    MODEL VARCHAR(100),
    FIRMWARE_VERSION VARCHAR(50),
    INSTALL_DATE DATE,
    LATITUDE FLOAT,
    LONGITUDE FLOAT,
    REGION VARCHAR(50),
    DISTRICT VARCHAR(50),
    CUSTOMER_ID VARCHAR(50),
    STATUS VARCHAR(20) DEFAULT 'ACTIVE' COMMENT 'ACTIVE, INACTIVE, MAINTENANCE, DECOMMISSIONED',
    COMMUNICATION_TYPE VARCHAR(30) COMMENT 'GEN6_MESH, GEN5_MESH, CELLULAR, RF',
    DI_ENABLED BOOLEAN DEFAULT FALSE COMMENT 'Distributed Intelligence enabled',
    LAST_COMMUNICATION TIMESTAMP_NTZ,
    CREATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

-- Interval Meter Readings
CREATE OR REPLACE TABLE METER_READINGS (
    READING_ID VARCHAR(50) PRIMARY KEY,
    METER_ID VARCHAR(50) NOT NULL,
    READING_TIMESTAMP TIMESTAMP_NTZ NOT NULL,
    READING_VALUE FLOAT NOT NULL COMMENT 'Consumption value in native units',
    READING_UNIT VARCHAR(20) NOT NULL COMMENT 'KWH, THERMS, GALLONS, CUBIC_FEET',
    INTERVAL_MINUTES INTEGER DEFAULT 15 COMMENT 'Reading interval in minutes',
    QUALITY_FLAG VARCHAR(20) DEFAULT 'VALID' COMMENT 'VALID, ESTIMATED, SUSPECT, MISSING',
    DEMAND_KW FLOAT COMMENT 'Demand in kilowatts (electric only)',
    VOLTAGE FLOAT COMMENT 'Voltage reading (electric only)',
    FLOW_RATE FLOAT COMMENT 'Flow rate (water/gas only)',
    PRESSURE_PSI FLOAT COMMENT 'Pressure reading (water/gas only)',
    TEMPERATURE_F FLOAT COMMENT 'Temperature at meter',
    TAMPER_FLAG BOOLEAN DEFAULT FALSE,
    CREATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

-- IoT Sensor Events
CREATE OR REPLACE TABLE SENSOR_EVENTS (
    EVENT_ID VARCHAR(50) PRIMARY KEY,
    SENSOR_ID VARCHAR(50) NOT NULL,
    ASSET_ID VARCHAR(50),
    EVENT_TIMESTAMP TIMESTAMP_NTZ NOT NULL,
    EVENT_TYPE VARCHAR(50) NOT NULL COMMENT 'TEMPERATURE, PRESSURE, VIBRATION, VOLTAGE, CURRENT, FLOW, HUMIDITY',
    EVENT_VALUE FLOAT NOT NULL,
    EVENT_UNIT VARCHAR(20),
    SEVERITY VARCHAR(20) DEFAULT 'NORMAL' COMMENT 'NORMAL, WARNING, CRITICAL, ALARM',
    LOCATION_LAT FLOAT,
    LOCATION_LON FLOAT,
    REGION VARCHAR(50),
    CREATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

-- Grid and Infrastructure Assets
CREATE OR REPLACE TABLE GRID_ASSETS (
    ASSET_ID VARCHAR(50) PRIMARY KEY,
    ASSET_TYPE VARCHAR(50) NOT NULL COMMENT 'TRANSFORMER, SUBSTATION, PIPE_SEGMENT, VALVE, PUMP_STATION, REGULATOR',
    ASSET_NAME VARCHAR(200),
    INSTALL_DATE DATE,
    EXPECTED_LIFETIME_YEARS INTEGER,
    MANUFACTURER VARCHAR(100),
    MODEL VARCHAR(100),
    CAPACITY_RATING FLOAT COMMENT 'Rated capacity in native units',
    CAPACITY_UNIT VARCHAR(20),
    REGION VARCHAR(50),
    DISTRICT VARCHAR(50),
    LATITUDE FLOAT,
    LONGITUDE FLOAT,
    STATUS VARCHAR(20) DEFAULT 'OPERATIONAL' COMMENT 'OPERATIONAL, DEGRADED, FAILED, MAINTENANCE',
    LAST_INSPECTION_DATE DATE,
    CONDITION_SCORE FLOAT COMMENT 'Score 0-100 from last inspection',
    CREATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

-- Work Orders and Maintenance
CREATE OR REPLACE TABLE WORK_ORDERS (
    WORK_ORDER_ID VARCHAR(50) PRIMARY KEY,
    ASSET_ID VARCHAR(50),
    METER_ID VARCHAR(50),
    ORDER_TYPE VARCHAR(50) NOT NULL COMMENT 'PREVENTIVE, CORRECTIVE, EMERGENCY, INSPECTION, INSTALL, DECOMMISSION',
    PRIORITY VARCHAR(20) NOT NULL COMMENT 'CRITICAL, HIGH, MEDIUM, LOW',
    STATUS VARCHAR(20) NOT NULL COMMENT 'OPEN, IN_PROGRESS, COMPLETED, CANCELLED',
    CREATED_DATE TIMESTAMP_NTZ NOT NULL,
    SCHEDULED_DATE DATE,
    COMPLETED_DATE TIMESTAMP_NTZ,
    TECHNICIAN_ID VARCHAR(50),
    DESCRIPTION VARCHAR(2000),
    ROOT_CAUSE VARCHAR(500),
    RESOLUTION VARCHAR(1000),
    COST_ESTIMATE FLOAT,
    ACTUAL_COST FLOAT,
    REGION VARCHAR(50),
    CREATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

-- Outage Events
CREATE OR REPLACE TABLE OUTAGE_EVENTS (
    OUTAGE_ID VARCHAR(50) PRIMARY KEY,
    OUTAGE_TYPE VARCHAR(30) NOT NULL COMMENT 'ELECTRIC, WATER, GAS',
    CAUSE VARCHAR(100) COMMENT 'WEATHER, EQUIPMENT_FAILURE, PLANNED, VEHICLE, ANIMAL, UNKNOWN',
    START_TIME TIMESTAMP_NTZ NOT NULL,
    END_TIME TIMESTAMP_NTZ,
    DURATION_MINUTES FLOAT,
    CUSTOMERS_AFFECTED INTEGER,
    REGION VARCHAR(50),
    DISTRICT VARCHAR(50),
    AFFECTED_ASSETS VARCHAR(2000) COMMENT 'Comma-separated asset IDs',
    RESTORATION_PRIORITY VARCHAR(20),
    CREW_DISPATCHED BOOLEAN DEFAULT FALSE,
    CREATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

-- Utility Customer Accounts
CREATE OR REPLACE TABLE CUSTOMERS (
    CUSTOMER_ID VARCHAR(50) PRIMARY KEY,
    CUSTOMER_NAME VARCHAR(200),
    CUSTOMER_TYPE VARCHAR(30) NOT NULL COMMENT 'RESIDENTIAL, COMMERCIAL, INDUSTRIAL, MUNICIPAL',
    SERVICE_ADDRESS VARCHAR(500),
    REGION VARCHAR(50),
    DISTRICT VARCHAR(50),
    ACCOUNT_STATUS VARCHAR(20) DEFAULT 'ACTIVE',
    SERVICE_START_DATE DATE,
    RATE_CLASS VARCHAR(50),
    HAS_SOLAR BOOLEAN DEFAULT FALSE,
    HAS_EV BOOLEAN DEFAULT FALSE,
    HAS_BATTERY_STORAGE BOOLEAN DEFAULT FALSE,
    DEMAND_RESPONSE_ENROLLED BOOLEAN DEFAULT FALSE,
