-- ============================================================================
-- ONTOLOGY SCHEMA - ESGOnt Ontology Tables
-- ============================================================================
USE SCHEMA ITRON_DB.ONTOLOGY;

-- OWL Classes from ESGOnt
CREATE OR REPLACE TABLE ONTOLOGY_CLASSES (
    CLASS_ID VARCHAR(100) PRIMARY KEY,
    CLASS_NAME VARCHAR(200) NOT NULL,
    CLASS_URI VARCHAR(500) NOT NULL,
    PARENT_CLASS_ID VARCHAR(100) COMMENT 'Direct superclass',
    ESG_PILLAR VARCHAR(30) COMMENT 'ENVIRONMENTAL, SOCIAL, GOVERNANCE',
    HIERARCHY_LEVEL INTEGER COMMENT 'Depth in class tree (0=root)',
    DESCRIPTION VARCHAR(2000),
    CREATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

-- Object and Data Properties
CREATE OR REPLACE TABLE ONTOLOGY_PROPERTIES (
    PROPERTY_ID VARCHAR(100) PRIMARY KEY,
    PROPERTY_NAME VARCHAR(200) NOT NULL,
    PROPERTY_URI VARCHAR(500) NOT NULL,
    PROPERTY_TYPE VARCHAR(20) NOT NULL COMMENT 'OBJECT or DATA',
    DOMAIN_CLASS_ID VARCHAR(100) COMMENT 'Class this property belongs to',
    RANGE_CLASS_ID VARCHAR(100) COMMENT 'Class this property points to (object) or datatype (data)',
    DESCRIPTION VARCHAR(2000),
    CREATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

-- Class-to-Class Relationships (subClassOf, equivalentClass, etc.)
CREATE OR REPLACE TABLE ONTOLOGY_RELATIONSHIPS (
    RELATIONSHIP_ID VARCHAR(100) PRIMARY KEY,
    SOURCE_CLASS_ID VARCHAR(100) NOT NULL,
    TARGET_CLASS_ID VARCHAR(100) NOT NULL,
    RELATIONSHIP_TYPE VARCHAR(50) NOT NULL COMMENT 'SUBCLASS_OF, EQUIVALENT_TO, DISJOINT_WITH, PART_OF',
    PROPERTY_ID VARCHAR(100) COMMENT 'The property defining this relationship',
    DESCRIPTION VARCHAR(2000),
    CREATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

-- ESG Metric to SDG Mapping (Deterministic Rules)
CREATE OR REPLACE TABLE ONTOLOGY_SDG_MAPPINGS (
    MAPPING_ID VARCHAR(100) PRIMARY KEY,
    ESG_METRIC_NAME VARCHAR(200) NOT NULL,
    ESG_PILLAR VARCHAR(30) NOT NULL,
    ESG_CATEGORY VARCHAR(100) NOT NULL,
    SDG_GOAL_NUMBER INTEGER NOT NULL,
    SDG_GOAL_NAME VARCHAR(200) NOT NULL,
    SDG_TARGET VARCHAR(20),
    SDG_INDICATOR VARCHAR(50),
    MAPPING_STRENGTH VARCHAR(20) NOT NULL COMMENT 'DIRECT, STRONG, MODERATE, INDIRECT',
    VALIDATION_RULE VARCHAR(2000) COMMENT 'Rule description for constraint validation',
    CREATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

-- Validation Rules for LLM Constraint
CREATE OR REPLACE TABLE ONTOLOGY_VALIDATION_RULES (
    RULE_ID VARCHAR(100) PRIMARY KEY,
    RULE_NAME VARCHAR(200) NOT NULL,
    RULE_TYPE VARCHAR(50) NOT NULL COMMENT 'CLASSIFICATION, UNIT, SDG_ALIGNMENT, HIERARCHY, THRESHOLD',
    RULE_CATEGORY VARCHAR(100) NOT NULL COMMENT 'The ESG category this rule applies to',
    CONDITION_FIELD VARCHAR(200) NOT NULL COMMENT 'The field/metric being validated',
    EXPECTED_VALUE VARCHAR(500) NOT NULL COMMENT 'The correct/expected value or pattern',
    ERROR_MESSAGE VARCHAR(1000) NOT NULL COMMENT 'Message when validation fails',
    SEVERITY VARCHAR(20) DEFAULT 'ERROR' COMMENT 'ERROR, WARNING, INFO',
    IS_ACTIVE BOOLEAN DEFAULT TRUE,
    CREATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);

-- Standard Metric Definitions with Units
CREATE OR REPLACE TABLE ONTOLOGY_METRIC_DEFINITIONS (
    DEFINITION_ID VARCHAR(100) PRIMARY KEY,
    METRIC_NAME VARCHAR(200) NOT NULL,
    METRIC_DESCRIPTION VARCHAR(2000) NOT NULL,
    ESG_PILLAR VARCHAR(30) NOT NULL,
    ESG_CATEGORY VARCHAR(100) NOT NULL,
    ESG_SUBCATEGORY VARCHAR(100),
    STANDARD_UNIT VARCHAR(50) NOT NULL,
    CALCULATION_FORMULA VARCHAR(1000),
    REPORTING_FRAMEWORK VARCHAR(100) COMMENT 'GRI, ESRS, SASB, TCFD, CDP',
    FRAMEWORK_INDICATOR_ID VARCHAR(50) COMMENT 'Standard indicator code (e.g., GRI 305-1)',
    DATA_TYPE VARCHAR(30) DEFAULT 'QUANTITATIVE' COMMENT 'QUANTITATIVE, QUALITATIVE, BINARY',
    FREQUENCY VARCHAR(20) DEFAULT 'QUARTERLY' COMMENT 'ANNUAL, QUARTERLY, MONTHLY',
    CREATED_AT TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP()
);
